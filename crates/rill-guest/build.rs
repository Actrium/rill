/// Generate `store_net_contract.rs` (the RBS1 binary-value ENVELOPE magic,
/// protocol version and fail-closed caps) from `contracts/store-net-bytes.json`,
/// the single authoritative source for the R2 first-class-bytes wire. A THIRD
/// sibling to op-batch-wire.json / canvas-wire.json — same single-source +
/// golden-lock discipline, DISTINCT magic (`RBS1` = 0x31534252, differing from
/// `RILL`/`RCNV` at byte 1) so the host's u32 magic fork routes it
/// unambiguously. Unlike those two, RBS1 is not an opcode stream: it is a
/// generic JSON-control-plane + binary-data-plane frame, so ONLY the magic,
/// version and caps are codegen'd here (there are no opcodes/flags/tags). Same
/// rationale as the peers: the codec's magic and caps are whatever the contract
/// says, so they cannot drift, and a renamed/removed key fails the build here.
fn generate_store_net_contract(manifest_dir: &str, out_dir: &str) {
    let contract_path = Path::new(manifest_dir).join("../../contracts/store-net-bytes.json");
    println!("cargo:rerun-if-changed={}", contract_path.display());

    let src = fs::read_to_string(&contract_path)
        .unwrap_or_else(|e| panic!("read {}: {e}", contract_path.display()));
    let root =
        Json::parse(&src).unwrap_or_else(|e| panic!("parse {}: {e}", contract_path.display()));

    let mut out = String::from(
        "// @generated by crates/rill-guest/build.rs from contracts/store-net-bytes.json.\n\
         // Do not edit; change the contract instead (it is the single source of truth\n\
         // for this codec AND the TS envelope codec + host validators).\n\n",
    );

    // Derive MAGIC from the contract's `hex` field and cross-check its LE bytes
    // spell `asciiBytes`, so a future edit to either field fails the build. This
    // magic (0x31534252 = 'RBS1', bytes 52 42 53 31) is DISTINCT from op-batch's
    // 0x4c4c4952 ('RILL') and canvas's 0x564e4352 ('RCNV'): all three share byte
    // 0 (0x52 'R') but differ at byte 1, so a u32 magic compare disambiguates.
    // The 4th magic byte ('1' = 0x31) carries the wire version — there is no
    // separate version field on the wire.
    let magic_node = root.get("magic").expect("contract: magic missing");
    let magic_hex = magic_node
        .get("hex")
        .and_then(Json::as_str)
        .expect("contract: magic.hex missing");
    let magic = u32::from_str_radix(magic_hex.trim_start_matches("0x"), 16)
        .unwrap_or_else(|e| panic!("contract: magic.hex {magic_hex:?} not hex: {e}"));
    let ascii = magic_node
        .get("asciiBytes")
        .and_then(Json::as_str)
        .expect("contract: magic.asciiBytes missing");
    let ascii_from_hex: String = magic
        .to_le_bytes()
        .iter()
        .map(|b| format!("{b:02X}"))
        .collect::<Vec<_>>()
        .join(" ");
    assert_eq!(
        ascii_from_hex, ascii,
        "contract: magic.hex LE bytes disagree with magic.asciiBytes"
    );
    out.push_str(&format!(
        "/// First 4 bytes of every RBS1 envelope, read as a u32 LE ('RBS1',\n\
         /// bytes 52 42 53 31). Reject on mismatch. DISTINCT from op-batch's MAGIC\n\
         /// (0x4c4c4952 'RILL') and canvas's (0x564e4352 'RCNV') so the host's u32\n\
         /// magic fork routes a frame to exactly one decoder. The 4th byte ('1')\n\
         /// carries the wire version — there is no separate version field.\n\
         pub const MAGIC: u32 = {magic};\n"
    ));

    let version = root
        .get("protocolVersion")
        .and_then(|v| v.get("value"))
        .and_then(Json::as_u64)
        .expect("contract: protocolVersion.value missing");
    out.push_str(&format!(
        "/// Envelope version, mirrored from the 4th magic byte (this codec speaks v1\n\
         /// only). There is NO separate protocolVersion field on the wire — a v2\n\
         /// frame is 'RBS2', a different MAGIC an old decoder rejects on compare.\n\
         pub const PROTOCOL_VERSION: u16 = {version};\n\n"
    ));

    out.push_str(
        "/// Fail-closed structural caps (single-sourced; the guest and host MUST NOT\n\
         /// drift). On any breach the codec aborts the whole frame and lets nothing\n\
         /// partial cross the seam; a conformance test locks these to the contract.\n\
         pub mod limits {\n",
    );
    let limits = root.get("limits").expect("contract: limits missing");
    let entries = limits
        .entries()
        .expect("contract: limits must be an object");
    let mut emitted = 0usize;
    for (name, spec) in entries {
        if name.starts_with('$') {
            continue;
        }
        let value = spec
            .get("value")
            .and_then(Json::as_u64)
            .unwrap_or_else(|| panic!("contract: limits.{name}.value missing"));
        // These caps are all byte/count magnitudes well within u32; emit as
        // `usize` so they compare directly against a buffer `.len()`.
        out.push_str(&format!(
            "    pub const {}: usize = {value};\n",
            screaming_snake(name)
        ));
        emitted += 1;
    }
    assert!(emitted > 0, "contract: limits is empty");
    out.push_str("}\n");

    let out_path = Path::new(out_dir).join("store_net_contract.rs");
    fs::write(&out_path, out).unwrap_or_else(|e| panic!("write {}: {e}", out_path.display()));
}
